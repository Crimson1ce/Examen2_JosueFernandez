    private void bt_crearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_crearUsuarioActionPerformed
        try {
            String nombre = tf_nombre_Crear.getText().trim();
            String apellido = tf_apellido_Crear.getText().trim();
            int numeroTelefonico = Integer.parseInt(ff_telefono_Crear.getValue().toString());
            String usuario = tf_usuario_Crear.getText().trim();
            String contrase침a = pf_contra_Crear.getText().trim();
            String confirmacion = pf_confirmacion_Crear.getText().trim();
            int calidad = Integer.parseInt(sp_conexion_Crear.getValue().toString());

            if (!nombre.equals("") && !apellido.equals("") && !usuario.equals("") && !contrase침a.equals("")) { //Confirmamos que no haya campos vac칤os
                if (usuarioValido(usuario)) { //Confirmamos que el usuario sea 칰nico
                    if (contrase침a.equals(confirmacion)) { //Verificamos la confirmaci칩n de la contrase침a
                        Usuario user = new Usuario(usuario, contrase침a, calidad, nombre, apellido, numeroTelefonico);
                        usuarios.add(user);
                        usuarioActual = user;
                        jd_crearUsuario.setVisible(false);
                        limpiarCreacion();
                        ingresar();
                        escribirUsuarios();
                    } else {
                        JOptionPane.showMessageDialog(jd_crearUsuario, "Error al confirmar la contrase침a.", "Confirmaci칩n fallida", 0);
                    }
                } else {
                    JOptionPane.showMessageDialog(jd_crearUsuario, "El usuario ingresado ya existe.", "Usuario inv치lido", 0);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_bt_crearUsuarioActionPerformed

